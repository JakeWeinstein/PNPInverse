@pytest.mark.slow
class TestKnownParameterRoundtripPDE:
    """Starting the PDE optimizer at the true parameters should converge immediately.

    Mathematical property tested:
        If the target I-V curve is generated by the PDE solver at theta*,
        and the optimizer is initialised at theta*, then the gradient should
        be zero (up to FD approximation error) and the loss should be zero.
        The optimizer should terminate in 0-1 iterations.

    Tolerance:
        - Loss < 1e-10: at truth, PDE(theta*) - target = 0 exactly.
        - Iterations <= 1: the gradient at the minimum is zero, so L-BFGS-B
          should stop immediately or after one line-search step.

    This test uses a small voltage grid (5 points) to keep PDE solves fast
    (~30s total instead of ~300s for the full grid).
    """

    def test_pde_at_truth_converges_immediately(self):
        """PDE optimizer starting at truth should report near-zero loss."""
        try:
            import firedrake  # noqa: F401
        except ImportError:
            pytest.skip("Firedrake not available")

        setup_firedrake_env()

        from Forward.steady_state import SteadyStateConfig
        from Forward.bv_solver import make_graded_rectangle_mesh
        from FluxCurve.bv_point_solve import (
            solve_bv_curve_points_with_warmstart,
            _clear_caches,
        )

        # Small voltage grid: 5 moderately cathodic points
        # Using dimensionless overpotentials (eta_hat = eta / V_T)
        phi_applied = np.array([-2.0, -5.0, -8.0, -12.0, -16.0])

        dt = 0.5
        max_ss_steps = 100
        t_end = dt * max_ss_steps

        base_sp = make_bv_solver_params(
            eta_hat=0.0, dt=dt, t_end=t_end,
            species=FOUR_SPECIES_CHARGED, snes_opts=SNES_OPTS_CHARGED,
        )

        steady = SteadyStateConfig(
            relative_tolerance=1e-4, absolute_tolerance=1e-8,
            consecutive_steps=4, max_steps=max_ss_steps,
            flux_observable="total_species", verbose=False,
        )

        mesh = make_graded_rectangle_mesh(Nx=8, Ny=200, beta=3.0)
        recovery = make_recovery_config(max_it_cap=600)
        dummy_target = np.zeros_like(phi_applied, dtype=float)

        # Generate PDE targets at the true parameters
        targets = {}
        for obs_mode in ["current_density", "peroxide_current"]:
            _clear_caches()
            points = solve_bv_curve_points_with_warmstart(
                base_solver_params=base_sp,
                steady=steady,
                phi_applied_values=phi_applied,
                target_flux=dummy_target,
                k0_values=[K0_HAT_R1, K0_HAT_R2],
                blob_initial_condition=False,
                fail_penalty=1e9,
                forward_recovery=recovery,
                observable_mode=obs_mode,
                observable_reaction_index=None,
                observable_scale=-I_SCALE,
                mesh=mesh,
                alpha_values=[ALPHA_R1, ALPHA_R2],
                control_mode="joint",
                max_eta_gap=3.0,
            )
            targets[obs_mode] = np.array(
                [float(p.simulated_flux) for p in points], dtype=float
            )

        _clear_caches()

        # Now evaluate the objective at the true parameters
        # The loss should be exactly zero (same solver, same parameters)
        points_at_truth = solve_bv_curve_points_with_warmstart(
            base_solver_params=base_sp,
            steady=steady,
            phi_applied_values=phi_applied,
            target_flux=targets["current_density"],
            k0_values=[K0_HAT_R1, K0_HAT_R2],
            blob_initial_condition=False,
            fail_penalty=1e9,
            forward_recovery=recovery,
            observable_mode="current_density",
            observable_reaction_index=None,
            observable_scale=-I_SCALE,
            mesh=mesh,
            alpha_values=[ALPHA_R1, ALPHA_R2],
            control_mode="joint",
            max_eta_gap=3.0,
        )

        # Compute residual: simulated - target should be zero
        sim_cd = np.array(
            [float(p.simulated_flux) for p in points_at_truth], dtype=float
        )
        residual = sim_cd - targets["current_density"]
        loss = 0.5 * np.sum(residual ** 2)

        assert loss < 1e-10, (
            f"PDE loss at true parameters is {loss:.4e}, expected < 1e-10. "
            f"Residual norms: {np.abs(residual).tolist()}"
        )


