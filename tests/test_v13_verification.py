# Test 7: Multistart convergence basin
# ---------------------------------------------------------------------------

@pytest.mark.slow
class TestMultistartConvergenceBasin:
    """Multiple random starts should converge to the same minimum.

    Mathematical property tested:
        If the surrogate landscape has a single dominant basin near the true
        parameters, then different initial guesses should all converge to
        approximately the same optimum. If they converge to different optima,
        the landscape has multiple local minima (problematic for identifiability).

    This validates:
        - The objective has a unique global minimum near truth
        - The L-BFGS-B optimizer reliably finds it
        - The Latin Hypercube sampling provides adequate coverage

    Tolerance:
        - Parameter agreement within 10% relative error across all 5 runs.
          Justified because L-BFGS-B on a smooth surrogate should converge
          to the same point regardless of initialization.
        - Best loss within 2x of each other: if one run finds a dramatically
          better minimum, the others are stuck in local optima.
    """

    def test_multistart_agreement(self, nn_ensemble, true_predictions):
        """5 L-BFGS-B runs from diverse starts should converge to the same minimum.

        Uses direct scipy.optimize.minimize with SurrogateObjective to avoid
        the predict_batch path in run_multistart_inference, which triggers
        PyTorch/MPI segfaults in some environments.
        """
        from Surrogate.objectives import SurrogateObjective
        from scipy.optimize import minimize

        target_cd = true_predictions["current_density"]
        target_pc = true_predictions["peroxide_current"]

        # Training bounds from the ensemble
        tb = nn_ensemble.training_bounds
        if tb is not None:
            bounds_k0_1 = tb["k0_1"]
            bounds_k0_2 = tb["k0_2"]
            bounds_alpha = tb.get("alpha_1", (0.1, 0.9))
        else:
            bounds_k0_1 = (K0_HAT_R1 * 0.01, K0_HAT_R1 * 100)
            bounds_k0_2 = (K0_HAT_R2 * 0.01, K0_HAT_R2 * 100)
            bounds_alpha = (0.1, 0.9)

        log_bounds = [
            (np.log10(bounds_k0_1[0]), np.log10(bounds_k0_1[1])),
            (np.log10(bounds_k0_2[0]), np.log10(bounds_k0_2[1])),
            bounds_alpha,
            bounds_alpha,
        ]

        obj = SurrogateObjective(
            surrogate=nn_ensemble,
            target_cd=target_cd,
            target_pc=target_pc,
            secondary_weight=1.0,
            fd_step=1e-5,
            log_space_k0=True,
        )

        # 5 diverse starting points spread across the parameter space
        rng = np.random.default_rng(42)
        starts = []
        for _ in range(5):
            x0 = np.array([
                rng.uniform(*log_bounds[0]),
                rng.uniform(*log_bounds[1]),
                rng.uniform(*log_bounds[2]),
                rng.uniform(*log_bounds[3]),
            ])
            starts.append(x0)

        results = []
        for x0 in starts:
            res = minimize(
                obj.objective,
                x0,
                jac=obj.gradient,
                method="L-BFGS-B",
                bounds=log_bounds,
                options={"maxiter": 100, "ftol": 1e-14, "gtol": 1e-8},
            )
            results.append(res)

        # Extract recovered parameters (physical space)
        k0_1s = np.array([10.0 ** r.x[0] for r in results])
        k0_2s = np.array([10.0 ** r.x[1] for r in results])
        a1s = np.array([r.x[2] for r in results])
        a2s = np.array([r.x[3] for r in results])
        losses = np.array([r.fun for r in results])

        # At least 3 of 5 runs should agree on parameters within 10%.
        # The surrogate landscape may have shallow local minima (especially
        # for k0_2), so we require a majority consensus rather than unanimity.
        ref_k0_1 = np.median(k0_1s)
        ref_k0_2 = np.median(k0_2s)
        ref_a1 = np.median(a1s)
        ref_a2 = np.median(a2s)

        n_agreeing = 0
        for i in range(5):
            relerr_k0_1 = abs(k0_1s[i] - ref_k0_1) / max(abs(ref_k0_1), 1e-30)
            relerr_k0_2 = abs(k0_2s[i] - ref_k0_2) / max(abs(ref_k0_2), 1e-30)
            relerr_a1 = abs(a1s[i] - ref_a1) / max(abs(ref_a1), 1e-30)
            relerr_a2 = abs(a2s[i] - ref_a2) / max(abs(ref_a2), 1e-30)
            max_relerr = max(relerr_k0_1, relerr_k0_2, relerr_a1, relerr_a2)
            if max_relerr < 0.10:
                n_agreeing += 1

        assert n_agreeing >= 3, (
            f"Only {n_agreeing}/5 runs agree within 10% of the median. "
            f"k0_1s={k0_1s.tolist()}, k0_2s={k0_2s.tolist()}, "
            f"a1s={a1s.tolist()}, a2s={a2s.tolist()}. "
            f"Suggests the surrogate landscape has many local minima."
        )

        # The best loss across all 5 runs should be near zero
        best_loss = np.min(losses)
        assert best_loss < 1e-4, (
            f"Best loss across 5 runs is {best_loss:.4e}. "
            f"Expected near-zero for noise-free surrogate targets."
        )

        # At least 3 runs should find a loss near zero
        n_good_loss = np.sum(losses < 1e-4)
        assert n_good_loss >= 3, (
            f"Only {n_good_loss}/5 runs found loss < 1e-4. "
            f"Losses: {losses.tolist()}"
        )
