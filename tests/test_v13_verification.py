@pytest.mark.slow
class TestZeroNoiseIdentity:
    """With zero noise, the surrogate optimizer should recover true parameters.

    Mathematical property tested:
        If the target data is generated by evaluating the surrogate at the
        true parameters theta*, then minimising
            J(theta) = 0.5 ||S(theta) - S(theta*)||^2
        should yield theta_opt = theta* (up to surrogate smoothness and
        optimizer tolerance).

    Tolerances:
        - Parameter relative error < 5%: justified because the NN surrogate
          has ~1-3% approximation error vs the PDE, and L-BFGS-B achieves
          high precision on smooth objectives.
        - Final loss < 1e-6: at the true parameters the loss should be
          essentially zero (limited only by float64 rounding in the surrogate
          forward pass).
    """

    def test_surrogate_roundtrip(self, nn_ensemble, true_predictions):
        """S1 (alpha-only) -> S2 (joint L-BFGS-B) recovers truth from noise-free targets."""
        from Surrogate.objectives import (
            AlphaOnlySurrogateObjective,
            SubsetSurrogateObjective,
        )
        from scipy.optimize import minimize

        target_cd = true_predictions["current_density"]
        target_pc = true_predictions["peroxide_current"]
        n_eta = nn_ensemble.n_eta
        subset_idx = np.arange(n_eta)

        # ------ S1: Alpha-only optimisation ------
        alpha_obj = AlphaOnlySurrogateObjective(
            surrogate=nn_ensemble,
            target_cd=target_cd,
            target_pc=target_pc,
            fixed_k0=(K0_HAT_R1, K0_HAT_R2),
            secondary_weight=1.0,
            fd_step=1e-5,
        )
        x0_alpha = np.array([0.5, 0.5])  # generic starting point
        res_s1 = minimize(
            alpha_obj.objective,
            x0_alpha,
            jac=alpha_obj.gradient,
            method="L-BFGS-B",
            bounds=[(0.1, 0.9), (0.1, 0.9)],
            options={"maxiter": 60, "ftol": 1e-14, "gtol": 1e-8},
        )
        alpha_s1 = res_s1.x

        # ------ S2: Joint 4-parameter optimisation ------
        joint_obj = SubsetSurrogateObjective(
            surrogate=nn_ensemble,
            target_cd=target_cd,
            target_pc=target_pc,
            subset_idx=subset_idx,
            secondary_weight=1.0,
            fd_step=1e-5,
            log_space_k0=True,
        )
        x0_joint = np.array([
            np.log10(K0_HAT_R1),
            np.log10(K0_HAT_R2),
            alpha_s1[0],
            alpha_s1[1],
        ])
        bounds_joint = [
            (np.log10(K0_HAT_R1 * 0.01), np.log10(K0_HAT_R1 * 100)),
            (np.log10(K0_HAT_R2 * 0.01), np.log10(K0_HAT_R2 * 100)),
            (0.1, 0.9),
            (0.1, 0.9),
        ]
        res_s2 = minimize(
            joint_obj.objective,
            x0_joint,
            jac=joint_obj.gradient,
            method="L-BFGS-B",
            bounds=bounds_joint,
            options={"maxiter": 100, "ftol": 1e-14, "gtol": 1e-8},
        )

        # Extract recovered parameters
        k0_1_rec = 10.0 ** res_s2.x[0]
        k0_2_rec = 10.0 ** res_s2.x[1]
        alpha_1_rec = res_s2.x[2]
        alpha_2_rec = res_s2.x[3]

        # --- Assertions ---
        # Relative error on each parameter < 5%
        k0_1_relerr = abs(k0_1_rec - K0_HAT_R1) / K0_HAT_R1
        k0_2_relerr = abs(k0_2_rec - K0_HAT_R2) / K0_HAT_R2
        a1_relerr = abs(alpha_1_rec - ALPHA_R1) / ALPHA_R1
        a2_relerr = abs(alpha_2_rec - ALPHA_R2) / ALPHA_R2

        assert k0_1_relerr < 0.05, (
            f"k0_1 relative error {k0_1_relerr:.4f} exceeds 5%. "
            f"Recovered {k0_1_rec:.6e}, true {K0_HAT_R1:.6e}"
        )
        assert k0_2_relerr < 0.05, (
            f"k0_2 relative error {k0_2_relerr:.4f} exceeds 5%. "
            f"Recovered {k0_2_rec:.6e}, true {K0_HAT_R2:.6e}"
        )
        assert a1_relerr < 0.05, (
            f"alpha_1 relative error {a1_relerr:.4f} exceeds 5%. "
            f"Recovered {alpha_1_rec:.6f}, true {ALPHA_R1:.6f}"
        )
        assert a2_relerr < 0.05, (
            f"alpha_2 relative error {a2_relerr:.4f} exceeds 5%. "
            f"Recovered {alpha_2_rec:.6f}, true {ALPHA_R2:.6f}"
        )

        # Final loss should be near machine zero
        assert res_s2.fun < 1e-6, (
            f"Final surrogate loss {res_s2.fun:.4e} exceeds 1e-6. "
            f"Expected near-zero loss when target = surrogate(truth)."
        )


